package org.strg;
public class Charindexofstring {
	public static void main(String[] args) {
		String s1="Green Technology";
		String s2="SeleniumAutomationtool";
		String s3="velmurugan";
		String s4="j a v a p r o g r a m";
		String s5="741852963";
		System.out.println("last index of o in string 1= "+s1.lastIndexOf('o'));
   System.out.println("index of o in string 2= "+s2.indexOf('o'));
   System.out.println("index value of n in string 3 = "+s3.indexOf('n'));
   System.out.println("last index value of  empty space in string 4 = "+s4.lastIndexOf(' '));
  System.out.println("index of 8 in string 5 ="+s5.indexOf('8'));
	}
}

