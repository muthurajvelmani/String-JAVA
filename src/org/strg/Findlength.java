package org.strg;
public class Findlength {
public static void main(String[] args) {
	String s1="Green Technology";
	String s2="SeleniumAutomationtool";
	String s3="velmurugan";
	String s4="j a v a p r o g r a m";
	String s5="741852963";
	System.out.println("string 1 length = "+s1.length());
	System.out.println("string 2 length = "+s2.length());
	System.out.println("string 3 length = "+s3.length());
	System.out.println("string 4 length = "+s4.length());
	System.out.println("string 5 length = "+s5.length());
}
}
